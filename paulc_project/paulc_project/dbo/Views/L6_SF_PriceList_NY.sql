CREATE VIEW [dbo].[L6_SF_PriceList_NY]
AS
SELECT  
	* --CustomerName, [Account Code], ITEMRELATION, ACCOUNTRELATION, INVENTLOCATIONID, Currency, UNITID, ItemDescription, AMOUNT, FROMDATE, TODATE, DATAAREAID, ModifiedDatetime
FROM    
	dbo.L4_SF_PriceList_NY
WHERE  
	(ModifiedDatetime > (
		SELECT ISNULL(
			(SELECT  MODIFIEDDATETIME
              FROM     dbo.Incremental_Tracking
              WHERE   (VIEW_NAME = 'L4_SF_PriceList_NY'))
			  , (select CAST('20 Dec 2019' as datetime)	)
		)
	))



