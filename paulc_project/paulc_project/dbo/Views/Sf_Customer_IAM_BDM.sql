

CREATE VIEW [dbo].[Sf_Customer_IAM_BDM]
AS
SELECT        SU.Name, SA.Shipping_Account_Number__c, SA.Salesforce_Account_Number__c, SA.IAM_1__c, SA.LastModifiedDate, SA.Company__c
FROM            dbo.Sf_Account AS SA FULL OUTER JOIN
                         dbo.Sf_User AS SU ON SU.Id = SA.OwnerId
WHERE        (SA.RecordTypeId LIKE '%012F0000000zfbM%')
			and SU.Name is not null 
			and SA.Shipping_Account_Number__c is not null
			and SA.Salesforce_Account_Number__c is not null
			and SA.IAM_1__c is not null
			and SA.Company__c is not null


